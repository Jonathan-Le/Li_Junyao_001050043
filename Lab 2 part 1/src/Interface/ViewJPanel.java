    public void populateTable(){
        
        DefaultTableModel dtm= (DefaultTableModel) TableVitalSign.getModel();
        
        dtm.setRowCount(0);
       
        for(VitalSigns sign : history.getVitalSignHistory()){
            Object row[] = new Object[2];
            row[0]=sign;
            row[1]=sign.getBloodPreasure();
            dtm.addRow(row);
        }
            
    }
