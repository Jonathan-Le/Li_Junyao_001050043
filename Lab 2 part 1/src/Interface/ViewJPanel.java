    private void ViewButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewButtonActionPerformed
        // TODO add your handling code here:
        
        
          int selectedrow = TableVitalSign.getSelectedRow();
          if (selectedrow>=0){
              
              VitalSigns vs = (VitalSigns)TableVitalSign.getValueAt(TableVitalSign.getSelectedRow(), 0);
              
              ViewBloodPressureTextField.setText(String.valueOf(vs.getBloodPreasure()));
              ViewPulseTextField.setText(String.valueOf(vs.getPulse()));
              ViewDateTextField.setText(vs.getDate());
              ViewtemperatureTextField.setText(String.valueOf(vs.getTemperature()));
        
              
          }
          else
              JOptionPane.showMessageDialog(null,"Please select any row");

        
    }//GEN-LAST:event_ViewButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        
         int selectedrow = TableVitalSign.getSelectedRow();
          if (selectedrow>=0){
              VitalSigns vs = (VitalSigns)TableVitalSign.getValueAt(TableVitalSign.getSelectedRow(), 0);
              history.deleteVitals(vs);
              JOptionPane.showMessageDialog(null,"Vital Sign has been deleted");
              populateTable();
          }
          else
              JOptionPane.showMessageDialog(null,"Please select any row");
    }//GEN-LAST:event_DeleteButtonActionPerformed


