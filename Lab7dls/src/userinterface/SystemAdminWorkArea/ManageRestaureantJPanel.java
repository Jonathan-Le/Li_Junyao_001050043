    private void selectjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectjButtonActionPerformed
        // TODO add your handling code here:
       int row = jTable1.getSelectedRow();
        String name = jTable1.getValueAt(row,0).toString();
        Restaurant res = system.getRestaurantDirectory().findRestaurant(name);
        UserAccount del = system.getUseraccountdirectory().findUser(name);
        
        viewusernamejTextField.setText(del.getUsername());
        viewpasswordjTextField.setText(del.getPassword());
        viewlocationjTextField.setText(res.getLocation());
        viewphonejTextField.setText(res.getPhone());
    }//GEN-LAST:event_selectjButtonActionPerformed

