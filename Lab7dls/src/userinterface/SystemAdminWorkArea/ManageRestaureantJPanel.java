public void populateRequestTable(){

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            RestaurantDirectory worklist= system.getRestaurantDirectory();
            
            if (worklist != null){      
                for(Restaurant request : worklist.getRestaurantDirectory()){
                    Object[] row = new Object[3];
                    row[0]=request.getName();
                    row[1]=request.getLocation();
                    row[2]=request.getPhone();
                    dtm.addRow(row);
                }
            }
          
    }
