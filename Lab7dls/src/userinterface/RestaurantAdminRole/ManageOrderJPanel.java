public void populateRequestTable(){

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);                     
//            Restaurant  res = system.getRestaurantDirectory().findRestaurant(account.getUsername());
            if (worklist != null){               
                for(commentrequest request : worklist.getWorkRequestList()){
                    Object[] row = new Object[5];
                    row[0]=request.getReceiver();    
//                    row[1]=request;      
                    row[2]=request.getOrder().toArray();      
                    row[3]=request.getStatus();      
                    row[4]=request.getResult();      
                    dtm.addRow(row);
                }
            }
          
    }
