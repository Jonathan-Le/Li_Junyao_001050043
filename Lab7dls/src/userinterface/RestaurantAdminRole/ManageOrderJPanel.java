    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sender= jTextField1.getText();
        UserAccount deliverman = system.getUserAccountDirectory().findUser(sender);
        if (deliverman.equals(null)) {
            JOptionPane.showMessageDialog(null, "Cannot find the man","Warning",JOptionPane.ERROR_MESSAGE);
        }
        int row = jTable1.getSelectedRow();
        int orderID = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        worklist.findrequest(orderID).setResult("accepted");
        worklist.findrequest(orderID).setSender(deliverman);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void CanceljButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CanceljButtonActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row","Warning",JOptionPane.ERROR_MESSAGE);
        }
        int orderID = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        worklist.removeorder(orderID); 
    }//GEN-LAST:event_CanceljButtonActionPerformed


